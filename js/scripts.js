var num1 = parseInt(prompt("enter a number:"));
var num2 = parseInt(prompt("enter another number"));

var add = function() {
  return(num1+num2);
};

var subtract = function(){
  return(num1-num2);
}

var multiply = function(){
  return(num1*num2);
}

var divide = function() {
  return(num1/num2);
}

var remainder = function () {
  return(num1%num2);
}

